        String b = tblksr.getValueAt(bar, 1).toString();
        String c = tblksr.getValueAt(bar, 2).toString();
        String d = tblksr.getValueAt(bar, 3).toString();
        String e = tblksr.getValueAt(bar, 4).toString();
        String f = tblksr.getValueAt(bar, 5).toString();
        String g = tblksr.getValueAt(bar, 6).toString();
        
        txtid.setText(a);
        txtnm.setText(b);
